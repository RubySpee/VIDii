var button1996 = document.querySelector("ol li:nth-of-type(1) button");

function showPika1() {
    var currentPika = document.querySelector("main > section:first-of-type img.show");
    if(currentPika) {
        currentPika.classList.remove("show")
    }

    var pika1 = document.querySelector("main > section:first-of-type img:nth-of-type(1)");
    pika1.classList.add("show");
}

button1996.addEventListener("click", showPika1);


