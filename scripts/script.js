const audio = document.getElementById ("audio");
const buttons = document.getElementById ("play");

buttons.addEventListener("click", togglePlay);

function togglePlay() {
    console.log ('test')
    if (audio.paused) {
        audio.play();
    }
    else {
        audio.pause();
    }
};

